    <h3>Space Station Crew to Relocate Soyuz, Make Room for New Crewmates</h3>
    <p class="date">Sep 22, 2021</p>
    <img src="/first-blog-javascript-gallery/public/photos-and-images/Artikel 2.jpg" alt="Picture of the Soyuz MS-18 crew ship" width="100%" />
    <p>
      Three residents of the International Space Station will take a short
      ride aboard a Soyuz MS-18 spacecraft Tuesday, Sept. 28, relocating the
      spacecraft to prepare for the arrival of the next set of station crew
      members.
    </p>
    <p>
      Expedition 65 flight engineers Mark Vande Hei of NASA and Oleg Novitskiy
      and Pyotr Dubrov of the Russian Space Agency Roscosmos will undock from
      the station’s Earth-facing Rassvet module at 8:21 a.m. EDT. They will
      dock again at the Nauka Multipurpose Laboratory Module at 9 a.m. This
      will be the first time a spacecraft has attached to the new Nauka
      module, which arrived at the station in July.
    </p>
    <p>
      Live coverage of the maneuver will begin at 8 a.m. on NASA Television,
      the NASA app, and the agency’s website.
    </p>
    <p>
      The relocation will free the Rassvet port for the docking of another
      Soyuz spacecraft, designated Soyuz MS-19, which will carry three Russian
      crew members to the station in October. Soyuz commander and cosmonaut
      Anton Shkaplerov of Roscosmos and spaceflight participants Klim Shipenko
      and Yulia Peresild are scheduled to launch to the station Tuesday, Oct.
      5, from the Baikonur Cosmodrome in Kazakhstan.
    </p>
    <p>
      This will be the 20th Soyuz port relocation in station history and the
      first since March 2021.
    </p>
    <p>
      Vande Hei and Dubrov are scheduled to remain aboard the station until
      March 2022. At the time of his return, Vande Hei will have set the
      record for the longest single spaceflight for an American. Novitskiy,
      Shipenko, and Peresild are scheduled to return to Earth in October
      aboard the Soyuz MS-18 spacecraft.
    </p>
    <p>
      For more than 20 years, humans have lived and worked continuously aboard
      the International Space Station, advancing scientific knowledge and
      demonstrating new technologies, making research breakthroughs not
      possible on Earth. As a global endeavor, 244 people from 19 countries
      have visited the unique microgravity laboratory that has hosted more
      than 3,000 research and educational investigations from researchers in
      108 countries and areas.
    </p>
