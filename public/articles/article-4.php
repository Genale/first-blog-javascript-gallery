    <h2>175 Years Ago: Astronomers Discover Neptune, the Eighth Planet</h2>
    <p class="date">Sep 22, 2021</p>
    <p>
      On the night of Sept. 23-24, 1846, astronomers discovered Neptune, the
      eighth planet orbiting around the Sun. The discovery was made based on
      mathematical calculations of its predicted position due to observed
      perturbations in the orbit of the planet Uranus. The discovery was made
      using a telescope since Neptune is too faint to be visible to the naked
      eye, owing to its great distance from the Sun. Astronomers soon
      discovered a moon orbiting Neptune, but it took more than a century to
      discover a second one. Our knowledge of distant Neptune greatly
      increased from the scientific observations made during Voyager 2’s flyby
      in 1989, including the discovery of five additional moons and
      confirmation of dark rings orbiting the planet.
    </p>
    <img src="/first-blog-javascript-gallery/public/photos-and-images/Astronomer article 4.jpg" alt="Astronomer Urbain Le Verrier" width="100%" />
    <p>
      With the 1781 discovery of Uranus, the number of known planets in the
      solar system grew to seven. As astronomers continued to observe the
      newly discovered planet, they noticed irregularities in its orbit that
      Newton’s law of universal gravitation could not fully explain. However,
      effects from the gravity of a more distant planet could explain these
      perturbances. By 1845, Uranus had completed nearly one full revolution
      around the Sun and astronomers Urbain Jean-Joseph Le Verrier in Paris
      and John Couch Adams in Cambridge, England, independently calculated the
      location of this postulated planet. Based on Le Verrier’s calculations,
      on the night of Sept. 23-24, 1846, astronomer Johann Gottfried Galle
      used the Fraunhofer telescope at the Berlin Observatory and made the
      first observations of the new planet, only 1 degree from its calculated
      position. In retrospect, following its formal discovery, it turned out
      that several astronomers, starting with Galileo Galilei in 1612, had
      observed Neptune too, but because of its slow motion relative to the
      background stars. did not recognize it as a planet.
    </p>
